    private void initTestCaseTreeFolder()
    {
    	try 
		{
			JunitReader.getInstance().extractTestCases();
		} 
		catch(IOException e)
		{
			JOptionPane.showMessageDialog(null, "Unable to find TestCase Jar under: libs folder", "No TestCase Found", JOptionPane.ERROR_MESSAGE);
			System.exit(0);
		}
    }
    
